/* Demos page — the REAL Cosmo widget, floating on a MacBook you can drag him around.

   The markup in demos.php mirrors src/renderer/index.html element-for-element (150px
   face column, the 48px control rail that slides open on HOVER, the 220px chat column,
   the tasks/sources panel + setup overlays). This script gives every control the same
   behaviour it has on the Mac, plus the two things that sell it as "really there":
     • DRAG    — grab his face and move him anywhere on the screen (clamped to it)
     • grow    — opening chat/panel/setup grows the widget to the app's REAL dims
                 (rail 48 · chat 220 · panel 320 · setup 360), like the OS window
     • hover   → rail reveals (CSS); gear + close fade in
     • 🎙 mic  → recording pulse + 'listening' → 'thinking' → answer (canned voice ask)
     • 🔊 mute → silences his voice; chat-only still works (caption + transcript)
     • 💬 chat → the dark chat column; typing routes the same intents he acts on
     • 📋 panel→ Tasks / Reminders / Notes / Sources (search results land in Sources)
     • ⚙ gear → Brain / Voice / Ears setup
   His voice is his REAL on-device Kokoro voice (3 voices, switchable), generated by
   scripts/gen-demo-voice.mjs. No bluff: the browser can't run a mic or a live LLM, so
   replies are a small canned set — his real lines, in his real voice — and the copy
   says so. Muted or out loud, every feature behaves identically. */
(function () {
  'use strict';
  const $ = (id) => document.getElementById(id);
  const stage = $('demo-eyes');
  if (!stage || !window.CosmoEyes) return;

  const eyes = new CosmoEyes(stage, { scale: 1.0, interactive: true });
  const cw = $('cw');
  const screenEl = $('mb-screen');
  const bubble = $('demo-bubble');
  const msgs = $('cw-msgs');
  if (cw) cw.style.touchAction = 'none';

  // ── cancellable timing ──────────────────────────────────────────────────────
  let timers = [];
  const clearAll = () => { timers.forEach(clearTimeout); timers = []; };
  const at = (ms, fn) => timers.push(setTimeout(fn, ms));

  // ── caption + chat transcript ────────────────────────────────────────────────
  const showBubble = (t) => { if (bubble) { bubble.textContent = t || ''; bubble.classList.toggle('show', !!t); } };
  function pushMsg(text, who) {
    if (!msgs) return;
    const d = document.createElement('div');
    d.className = 'cw-msg ' + (who === 'user' ? 'user' : 'bot');
    d.textContent = text;
    msgs.appendChild(d);
    msgs.scrollTop = msgs.scrollHeight;
  }

  // ── status pill — the SAME states Cosmo shows on the desktop. The search tool
  //    sets activity 'searching' (rendered literally as "🔍 searching…", per the
  //    chibi pack); a timer/pomodoro shows "<label> M:SS" with a ✕, ticking each sec. ─
  const statusEl = $('demo-status');
  let statusTimer = null;
  function hideStatus() {
    if (statusTimer) { clearInterval(statusTimer); statusTimer = null; }
    if (statusEl) { statusEl.hidden = true; statusEl.innerHTML = ''; }
  }
  function showSearching() { if (statusEl) { hideStatus(); statusEl.textContent = '🔍 searching…'; statusEl.hidden = false; } }
  function showTimer(totalSec, label) {
    if (!statusEl) return;
    hideStatus();
    let rem = totalSec;
    const fmt = (s) => `${label} ${Math.floor(s / 60)}:${String(s % 60).padStart(2, '0')}`;
    const t = document.createElement('span'); t.textContent = fmt(rem);
    const x = document.createElement('button'); x.className = 'cw-status__stop'; x.type = 'button'; x.textContent = '✕'; x.setAttribute('aria-label', 'Stop');
    x.addEventListener('click', (e) => { e.stopPropagation(); hideStatus(); });
    statusEl.innerHTML = ''; statusEl.appendChild(t); statusEl.appendChild(x); statusEl.hidden = false;
    statusTimer = setInterval(() => { rem--; t.textContent = fmt(Math.max(0, rem)); if (rem <= 0) hideStatus(); }, 1000);
  }

  // ── voice (real Kokoro clips via manifest) ────────────────────────────────────
  let manifest = null, activeVoice = null, muted = false, audio = null;
  const stopAudio = () => { if (audio) { try { audio.pause(); } catch (e) {} audio = null; } };
  const lineText = (intent) => (manifest && manifest.lines[intent]) || '';
  function clipFor(intent) {
    if (!manifest || !activeVoice) return null;
    const v = manifest.clips[activeVoice];
    return (v && v[intent]) || null;
  }
  // One line: caption + transcript + the 'speaking' mouth, with his real audio when
  // un-muted. Muted (or clip missing) → same animation timed to the clip, just silent.
  function speak(intent, done) {
    const text = lineText(intent);
    showBubble(text);
    pushMsg(text, 'bot');
    eyes.setState('speaking');
    stopAudio();
    const clip = clipFor(intent);
    const durMs = (clip && clip.durMs) || Math.max(1600, text.length * 55);
    const finish = () => { eyes.setState('idle'); at(2600, () => showBubble('')); if (typeof done === 'function') done(); };
    if (!muted && clip) {
      const a = new Audio(clip.file);
      audio = a;
      a.addEventListener('ended', () => { if (audio === a) { audio = null; finish(); } });
      a.addEventListener('error', () => { if (audio === a) { audio = null; } at(durMs, finish); });
      a.play().catch(() => { at(durMs, finish); });
    } else {
      at(durMs, finish);
    }
  }

  // ── intent routing — the same buckets Cosmo acts on, matched loosely ──────────
  function route(text) {
    const t = ' ' + text.toLowerCase().replace(/[^\p{L}\p{N}\s]/gu, ' ').replace(/\s+/g, ' ') + ' ';
    if (/\b(find|search|look up|google|papers?|paper|articles?|news about|web)\b/.test(t)) return 'search';
    if (/\b(pomodoro|focus session|timer|minutes?|minute|countdown|alarm)\b/.test(t)) return 'timer';
    if (/\b(note|remember|write down|jot|save this|dont forget|remind me to)\b/.test(t)) return 'note';
    if (/\b(what are you|who are you|are you a|are you an|can you see|can you hear|what can you do)\b/.test(t)) return 'whatareyou';
    if (/\b(hi|hello|hey|hiya|yo|howdy|morning|evening)\b/.test(t)) return 'greet';
    return 'fallback';
  }

  // ── sources (real arXiv papers) — these land in the Sources tab after a search ─
  const SOURCES = [
    { title: 'Attention Is All You Need', host: 'arxiv.org', url: 'https://arxiv.org/abs/1706.03762' },
    { title: 'A Survey of Large Language Models', host: 'arxiv.org', url: 'https://arxiv.org/abs/2303.18223' },
    { title: 'LLaMA: Open & Efficient Foundation Models', host: 'arxiv.org', url: 'https://arxiv.org/abs/2302.13971' },
  ];
  function fillSources() {
    const list = $('cw-sources');
    if (!list) return;
    list.innerHTML = '';
    SOURCES.forEach((s) => {
      const item = document.createElement('div'); item.className = 'cw-item';
      const txt = document.createElement('span'); txt.className = 'cw-itext';
      const a = document.createElement('a'); a.href = s.url; a.target = '_blank'; a.rel = 'noopener';
      a.textContent = s.title; a.style.color = '#2f6fd0'; a.style.textDecoration = 'none';
      const host = document.createElement('span'); host.className = 'cw-when'; host.textContent = s.host;
      txt.appendChild(a); txt.appendChild(host); item.appendChild(txt);
      list.appendChild(item);
    });
  }

  // ── open-state machine: chat column / panel / setup, with the widget grown to
  //    the app's REAL dimensions and the rail kept reachable beside the chat. ──
  let chatOpen = false, overlay = null /* 'panel' | 'setup' */, recording = false, sizeClampT = null;
  function recompute() {
    const chatVisible = chatOpen && !overlay;
    $('cw-chatcol').classList.toggle('is-open', chatVisible);
    $('cw-chat').classList.toggle('active', chatVisible);
    $('cw-panel').classList.toggle('active', overlay === 'panel');
    $('cw-gear').classList.toggle('active', overlay === 'setup');
    $('cw-panel-overlay').classList.toggle('is-open', overlay === 'panel');
    $('cw-setup-overlay').classList.toggle('is-open', overlay === 'setup');
    cw.classList.toggle('rail-open', recording || chatVisible);
    const state = overlay || (chatVisible ? 'chat' : 'face');
    cw.classList.toggle('s-chat', state === 'chat');
    cw.classList.toggle('s-panel', state === 'panel');
    cw.classList.toggle('s-setup', state === 'setup');
    clearTimeout(sizeClampT); sizeClampT = setTimeout(clampIntoScreen, 240);
  }
  function showPanelTab(tab) {
    document.querySelectorAll('[data-ptab]').forEach((b) => b.classList.toggle('is-on', b.dataset.ptab === tab));
    document.querySelectorAll('[data-ppane]').forEach((p) => p.classList.toggle('is-on', p.dataset.ppane === tab));
  }

  // ── the ask flow: listen → think → (side effect) → answer ─────────────────────
  function ask(text, opts) {
    text = (text || '').trim();
    if (!text) return;
    opts = opts || {};
    clearAll(); stopAudio();
    document.querySelectorAll('[data-mood]').forEach((x) => x.classList.remove('is-on'));
    hideStatus(); showBubble('');
    pushMsg(text, 'user');
    const intent = route(text);
    eyes.setState('listening');
    at(650, () => eyes.setState('thinking'));
    if (intent === 'search') {
      at(900,  () => showSearching());
      at(1700, () => fillSources());
      at(2300, () => {
        hideStatus();
        speak('search', () => { if (opts.flourish) { eyes.setState('happy'); eyes.pulse('heart'); at(1400, () => eyes.setState('idle')); } });
        overlay = 'panel'; chatOpen = false; recompute(); showPanelTab('sources'); // reveal where they landed
      });
    } else if (intent === 'timer') {
      const pomodoro = /\b(pomodoro|focus)\b/.test(text.toLowerCase());
      at(1450, () => { speak('timer'); showTimer(pomodoro ? 1500 : 600, pomodoro ? 'Focus' : 'Timer'); });
    } else {
      at(1450, () => speak(intent));
    }
  }

  // ── DRAG: grab his face, move him anywhere on the screen (clamped) ─────────────
  function applyPos(left, top) { cw.style.left = left + 'px'; cw.style.top = top + 'px'; }
  let pos = { left: 0, top: 0 };
  function clampIntoScreen() {
    if (!screenEl) return;
    const maxLeft = Math.max(0, screenEl.clientWidth - cw.offsetWidth);
    const maxTop = Math.max(0, screenEl.clientHeight - cw.offsetHeight);
    pos.left = Math.min(Math.max(0, pos.left), maxLeft);
    pos.top = Math.min(Math.max(0, pos.top), maxTop);
    applyPos(pos.left, pos.top);
  }
  function placeInitial() {
    if (!screenEl) return;
    pos.left = Math.round(screenEl.clientWidth * 0.46);
    pos.top = Math.round(screenEl.clientHeight * 0.24);
    clampIntoScreen();
  }
  let drag = null;
  function dragScale() { const sr = screenEl.getBoundingClientRect(); return sr.width / (screenEl.offsetWidth || sr.width) || 1; }
  function onDragMove(e) {
    if (!drag) return;
    const dx = (e.clientX - drag.x) / drag.scale, dy = (e.clientY - drag.y) / drag.scale;
    if (!drag.moved && Math.hypot(dx, dy) < 4) return;     // a real drag, not a pet-tap
    drag.moved = true;
    cw.classList.add('dragging');
    if (e.cancelable) e.preventDefault();
    pos.left = drag.left + dx; pos.top = drag.top + dy;
    clampIntoScreen();
  }
  function onDragUp() {
    window.removeEventListener('pointermove', onDragMove);
    const moved = drag && drag.moved;
    drag = null;
    cw.classList.remove('dragging');
    if (moved) { // swallow the click that trails a drag, so the eyes don't pet
      const supp = (ev) => { ev.stopPropagation(); ev.preventDefault(); };
      cw.addEventListener('click', supp, { capture: true, once: true });
      setTimeout(() => cw.removeEventListener('click', supp, true), 0);
    }
  }
  // Capture phase so we beat CosmoEyes' own handlers; WINDOW-level move/up so the drag
  // keeps tracking even when the eyes grab pointer capture (the old bug). Controls are
  // excluded — their clicks (gear/close/rail dots/chat/inputs) keep working untouched.
  cw.addEventListener('pointerdown', (e) => {
    if (e.button !== 0) return;
    if (e.target.closest('.cw-rail, .cw-chatcol, .cw-overlay, .cw-gear, .cw-close, .cw-dot, button, input, a, select, textarea')) return;
    drag = { x: e.clientX, y: e.clientY, left: pos.left, top: pos.top, scale: dragScale(), moved: false };
    window.addEventListener('pointermove', onDragMove);
    window.addEventListener('pointerup', onDragUp, { once: true });
    window.addEventListener('pointercancel', onDragUp, { once: true });
  }, true);
  window.addEventListener('resize', () => { clampIntoScreen(); });

  // ── rail controls ─────────────────────────────────────────────────────────────
  const mic = $('cw-mic');
  if (mic) mic.addEventListener('click', () => {
    if (recording) { recording = false; mic.classList.remove('recording'); eyes.setState('idle'); recompute(); return; }
    clearAll(); stopAudio(); hideStatus(); showBubble('');
    recording = true; mic.classList.add('recording'); eyes.setState('listening'); recompute();
    at(1500, () => { recording = false; mic.classList.remove('recording'); recompute(); ask('find me papers on large language models'); });
  });

  const mute = $('cw-mute');
  function setMuted(on) {
    muted = on; if (on) stopAudio();
    if (mute) { mute.classList.toggle('is-muted', on); mute.textContent = on ? '🔇' : '🔊'; mute.setAttribute('aria-pressed', on ? 'true' : 'false'); mute.title = on ? 'Voice off — chat only' : 'Mute — silence his voice'; }
  }
  if (mute) mute.addEventListener('click', () => setMuted(!muted));

  const chatBtn = $('cw-chat');
  if (chatBtn) chatBtn.addEventListener('click', () => { chatOpen = !chatOpen; if (chatOpen) overlay = null; recompute(); if (chatOpen) { const i = $('demo-input'); if (i) at(120, () => i.focus()); } });
  const chatClose = $('cw-chat-close');
  if (chatClose) chatClose.addEventListener('click', () => { chatOpen = false; recompute(); });

  const panelBtn = $('cw-panel');
  if (panelBtn) panelBtn.addEventListener('click', () => { overlay = overlay === 'panel' ? null : 'panel'; if (overlay) chatOpen = false; recompute(); });
  const panelClose = $('cw-panel-close');
  if (panelClose) panelClose.addEventListener('click', () => { overlay = null; recompute(); });

  const gear = $('cw-gear');
  if (gear) gear.addEventListener('click', () => { overlay = overlay === 'setup' ? null : 'setup'; if (overlay) chatOpen = false; recompute(); });
  const setupClose = $('cw-setup-close');
  if (setupClose) setupClose.addEventListener('click', () => { overlay = null; recompute(); });

  const closeBtn = $('cw-close');
  // stopPropagation so this very click doesn't bubble to the outside-click handler
  // (below) and immediately re-close the tray menu we're opening.
  if (closeBtn) closeBtn.addEventListener('click', (e) => { e.stopPropagation(); hideCosmo(true); });

  // ── close to the menu bar → the real tray icon + its menu (mirrors createTray) ──
  let hidden = false;
  const trayBtn = $('mb-tray');
  const trayMenu = $('mb-traymenu');
  function setHidden(on) {
    hidden = on;
    cw.style.display = on ? 'none' : 'flex';
    if (on) { clearAll(); stopAudio(); chatOpen = false; overlay = null; recording = false; if (mic) mic.classList.remove('recording'); hideStatus(); recompute(); }
    else { requestAnimationFrame(clampIntoScreen); }
    syncTrayLabels();
  }
  function hideCosmo(openMenu) { setHidden(true); if (openMenu) openTray(true); }
  function showCosmo() { setHidden(false); }
  function openTray(on) {
    if (!trayMenu) return;
    trayMenu.hidden = !on;
    if (trayBtn) { trayBtn.classList.toggle('is-open', on); trayBtn.setAttribute('aria-expanded', on ? 'true' : 'false'); }
    if (on) syncTrayLabels();
  }
  function syncTrayLabels() {
    if (!trayMenu) return;
    const set = (k, t) => { const b = trayMenu.querySelector('[data-tray="' + k + '"]'); if (b) b.textContent = t; };
    set('toggle', hidden ? 'Show Cosmo' : 'Hide Cosmo');
    set('chat', (chatOpen && !overlay) ? 'Close chat' : 'Open chat');
    set('mute', muted ? 'Unmute' : 'Mute (silence & stop listening)');
    const q = trayMenu.querySelector('[data-tray="quit"]'); if (q) q.classList.add('is-danger');
  }
  if (trayBtn) trayBtn.addEventListener('click', (e) => { e.stopPropagation(); openTray(trayMenu.hidden); });
  document.addEventListener('click', (e) => { if (trayMenu && !trayMenu.hidden && !e.target.closest('#mb-traymenu, #mb-tray')) openTray(false); });
  if (trayMenu) trayMenu.querySelectorAll('[data-tray]').forEach((b) => b.addEventListener('click', (e) => {
    e.stopPropagation();
    const a = b.dataset.tray;
    if (a === 'toggle') { hidden ? showCosmo() : hideCosmo(false); }
    else if (a === 'quit') { hideCosmo(false); }   // "quit" → tucked away; Show Cosmo brings him back
    else {
      if (hidden) showCosmo();
      if (a === 'mic' && mic) mic.click();
      else if (a === 'chat' && chatBtn) chatBtn.click();
      else if (a === 'panel' && panelBtn) panelBtn.click();
      else if (a === 'mute' && mute) mute.click();
      else if (a === 'setup' && gear) gear.click();
      else if (a === 'clear' && msgs) msgs.innerHTML = '';
    }
    openTray(false);
  }));

  // chat input
  const inputRow = $('cw-inputrow'); const input = $('demo-input');
  if (inputRow && input) inputRow.addEventListener('submit', (e) => { e.preventDefault(); const v = input.value; input.value = ''; ask(v); });

  // ── panel + setup tabs ─────────────────────────────────────────────────────────
  document.querySelectorAll('[data-ptab]').forEach((b) => b.addEventListener('click', () => showPanelTab(b.dataset.ptab)));
  document.querySelectorAll('[data-stab]').forEach((b) => b.addEventListener('click', () => {
    document.querySelectorAll('[data-stab]').forEach((x) => x.classList.toggle('is-on', x === b));
    document.querySelectorAll('[data-spane]').forEach((p) => p.classList.toggle('is-on', p.dataset.spane === b.dataset.stab));
  }));

  // ── chips + scripted scene ──────────────────────────────────────────────────────
  document.querySelectorAll('.demo-chip').forEach((c) => {
    if (c.id === 'demo-play') return;
    c.addEventListener('click', () => ask(c.dataset.ask || c.textContent));
  });
  const play = $('demo-play');
  if (play) play.addEventListener('click', () => ask('Cosmo, find me papers on large language models', { flourish: true }));

  // ── manual mood + reaction buttons (pure visual — the real eyes) ──────────────
  document.querySelectorAll('[data-mood]').forEach((b) => b.addEventListener('click', () => {
    clearAll(); stopAudio(); showBubble(''); hideStatus();
    document.querySelectorAll('[data-mood]').forEach((x) => x.classList.remove('is-on'));
    b.classList.add('is-on'); eyes.setState(b.dataset.mood);
  }));
  document.querySelectorAll('[data-pulse]').forEach((b) => b.addEventListener('click', () => eyes.pulse(b.dataset.pulse)));

  // ── voice picker + live preview ──────────────────────────────────────────────
  const voicebar = $('demo-voicebar'); const voiceSel = $('demo-voice'); const hearBtn = $('demo-hear');
  fetch('assets/media/voice/manifest.json', { cache: 'no-cache' })
    .then((r) => (r.ok ? r.json() : Promise.reject()))
    .then((m) => {
      manifest = m; activeVoice = m.defaultVoice;
      if (voiceSel) {
        voiceSel.innerHTML = '';
        m.voices.forEach((v) => { const o = document.createElement('option'); o.value = v.id; o.textContent = v.label; if (v.default) o.selected = true; voiceSel.appendChild(o); });
        voiceSel.addEventListener('change', () => { activeVoice = voiceSel.value; clearAll(); hideStatus(); speak('greet'); });
      }
      if (hearBtn) hearBtn.addEventListener('click', () => { clearAll(); hideStatus(); speak('greet'); });
      if (voicebar) voicebar.hidden = false;
    })
    .catch(() => { /* no audio generated — demo still works chat-only; picker stays hidden */ });

  // place Cosmo on the screen once layout is ready
  requestAnimationFrame(placeInitial);
})();
